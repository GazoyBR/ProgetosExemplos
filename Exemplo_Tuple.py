# Exemplo de uma tupla
dados_aluno = ('João', 20, 'Masculino', 'Engenharia')

# Imprimir a tupla
print("Dados do Aluno:", dados_aluno)

# Acessar elementos da tupla
nome = dados_aluno[0]
idade = dados_aluno[1]
genero = dados_aluno[2]
curso = dados_aluno[3]

# Imprimir elementos individuais
print("Nome:", nome)
print("Idade:", idade)
print("Gênero:", genero)
print("Curso:", curso)
