# Exemplo 1: Inteiro
numero = 42
tipo_numero = type(numero)
print(tipo_numero)  # Saída: <class 'int'>

# Exemplo 2: Ponto flutuante
numero_decimal = 3.14
tipo_decimal = type(numero_decimal)
print(tipo_decimal)  # Saída: <class 'float'>

# Exemplo 3: String
texto = "Olá, mundo!"
tipo_texto = type(texto)
print(tipo_texto)  # Saída: <class 'str'>
